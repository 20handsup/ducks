        <p>
          There is a famous cartoon duck named <em>Daffy.</em> He's black in color
          with a ring around his neck. Of course he has the usual yellow feet and
          bill. He has a <a href="#voice">funny voice</a>. Then there is
          <em>Donald Duck</em>, but really, he's just too much a cartoon. For one
          thing, he's depicted wearing a shirt and a hat. I think he's supposed to
          be a navy duck or something. And, I mean, <em>real</em> ducks don't wear
          clothes of any kind. Regardless, these are not the ducks we're concerned
          about here. We are really talking about real-live ducks.
        </p>
        <p>
          Ducks are rather large for birds. And are favorites for hunters. Maybe
          because they are very large (easier to hit with a shotgun) and they are
          generally shot when attempting to lite (or land) on ponds or other
          bodies of water.
        </p>
        <p>
          We here at DOPE (Ducks On Ponds Everywhere) believe that all ponds in
          our country should have ducks. It is our aim to see this come to
          fruition. After all, who among us doesn't enjoy watching ducks; they're
          fun! However, don't feed the ducks bread. When I was a kid, we used to
          go to the local park for purpose of feeding the ducks our old stale
          bread. Turns out, we learned later (much later) that ducks actually can
          get choked on bread. So, no more bread for the ducks.
        </p>
