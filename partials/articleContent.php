      <h1>Ducks On Ponds Everywhere</h1>
        <p>
          Ducks are migratory waterfowl. Most migrate, though some stay in one
          place year-around. They are not all white, but a lot of them are. They
          swim around in ponds, lakes and other bodies of water. They paddle
          around and make <em>quacking</em> (kuh-WAK-in) noises. It's believed by
          some that this is the way that they communicate with each other.
          However, it's never been proven.
        </p>
        <p>
          It's believed that ducks can really <em>boogie-down!</em> As a matter of
          fact, that is how the female ducks, or hens, choose a mate. By how well
          they dance. That and of course their appearance. Not unlike humans.
        </p>
        <p>
          Ducks have webbed feet. This allows them to move through water. They
          just sort of paddle around. That's where we get the term
          <em>duck paddle</em> Ducks also have a bill (mouth appendage) that is
          flat and wide, as opposed to the beaks of other fowl.
        </p>
        <p>
          Ducks sleep with one eye open. That's right. When they sleep, half their
          brain is asleep. The other half is awake and watching. This is why it is
          so hard to sneak-up on ducks. That's the thinking anyway.
        </p>
